phonebook = [
    {"ім'я": "Іван", "прізвище": "Петренко", "телефон": "123456789", "місто": "Київ"},
    {"ім'я": "Олена", "прізвище": "Іванова", "телефон": "987654321", "місто": "Львів"},
    {"ім'я": "Олег", "прізвище": "Сидоренко", "телефон": "555123456", "місто": "Харків"},
    {"ім'я": "Марія", "прізвище": "Коваль", "телефон": "111222333", "місто": "Київ"},
    {"ім'я": "Анна", "прізвище": "Мельник", "телефон": "444555666", "місто": "Одеса"}
]

key = input("Введіть ім'я, прізвище або місто для пошуку: ").strip()

if not key:
    print("Помилка: ви нічого не ввели.")
else:
    found = False
    for contact in phonebook:
        if key.lower() in (contact["ім'я"].lower(), contact["прізвище"].lower(), contact["місто"].lower()):
            if not found:
                print("\nЗнайдені контакти:")
                print("{:<10} {:<12} {:<12} {:<10}".format("Ім'я", "Прізвище", "Телефон", "Місто"))
                print("-" * 50)
                found = True
            print("{:<10} {:<12} {:<12} {:<10}".format(
                contact["ім'я"], contact["прізвище"], contact["телефон"], contact["місто"]
            ))
    if not found:
        print("Контакти не знайдено.")
